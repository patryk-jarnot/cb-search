ScoringMatrix::ScoringMatrix() {
	select(ScoringMatrix::Types::BLOSUM62);
}


float ScoringMatrix::get_score(char a, char b) {
	return current_matrix[((int)residue_to_matrix_index(a)) * SCORING_MATRIX_SIZE + ((int)residue_to_matrix_index(b))];
}

float ScoringMatrix::get_score(MatrixResidue a, MatrixResidue b) {
	return current_matrix[((int)a) * SCORING_MATRIX_SIZE + ((int)b)];
}


void ScoringMatrix::mask_residues_except_diagonal(std::vector<char> iresidues, float imask_value) {
	recalculator.mask_residues_except_diagonal(iresidues, imask_value);
	recalculator.recalculate(current_matrix);
	memcpy(current_matrix, recalculator.get_matrix(), SCORING_MATRIX_SIZE*SCORING_MATRIX_SIZE*sizeof(float));
}


void ScoringMatrix::apply_cbr_correction(std::string iquery, float ialpha) {
	recalculator.cbr_correction(iquery, ialpha);
	recalculator.recalculate(current_matrix);
	memcpy(current_matrix, recalculator.get_matrix(), SCORING_MATRIX_SIZE*SCORING_MATRIX_SIZE*sizeof(float));
}


void ScoringMatrix::select(ScoringMatrix::Types itype) {
	switch (itype) {
	case ScoringMatrix::Types::BLOSUM62:
		memcpy(current_matrix, blosum62_matrix, SCORING_MATRIX_SIZE*SCORING_MATRIX_SIZE*sizeof(float));
		break;
	case ScoringMatrix::Types::CUSTOM:
		throw NotImplemented(__FILE__, __LINE__);
	}
}




